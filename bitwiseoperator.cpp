#include<iostream>
using namespace std;
int main(){
    int a=5, b=3;
    //bitwise AND
    cout<<"value of a&b is:"<<(a&b)<<endl;
    //bitwise OR
    cout<<"value of a|b is:"<<(a|b)<<endl;
    //bitwise XOR
    cout<<"value of a^b is:"<<(a^b)<<endl;
    //bitwise left shift 
    cout<<"value of a>>1 is:"<<(a>>1)<<endl;
    //bitwise right shift 
    cout<<"value of a<<1 is:"<<(a<<1)<<endl;
    //bitwise NOT
    cout<<"value of ~a is:"<<(~a)<<endl;
    return 0;

}           