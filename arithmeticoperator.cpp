#include<iostream>
using namespace std;
int main(){
    int a=5,b=9;
    //addition
    cout<<"value of a+b:"<<a+b<<endl;
    //substraction
    cout<<"value of a-b:"<<a-b<<endl;
    //multiplication
    cout<<"value of a*b:"<<a*b<<endl;
    //division
    cout<<"value of a/b:"<<a/b<<endl;
    //modulus
    cout<<"value of a%b:"<<a%b<<endl;

    return 0;

}           